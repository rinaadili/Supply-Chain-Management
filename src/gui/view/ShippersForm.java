      public static boolean isValidEmailAddress(String email) {
            boolean result = true;
            try {
               InternetAddress emailAddr = new InternetAddress(email);
               emailAddr.validate();
            } catch (AddressException ex) {
               result = false;
            }
            return result;
        }
    
    private void tabelaSelectedIndexChange() {
        final ListSelectionModel rowSM = tabela.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {

            public void valueChanged(ListSelectionEvent Ise) {
                if (Ise.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) Ise.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    Shippers s = ctm.getShippers(selectedIndex);
                    ShippersTF.setText(s.getShipperID().toString());
                   CompanyNameTF.setText(s.getCompanyName());
                   PhoneTF.setText(s.getPhone());
                   EmailTF.setText(s.getEmail());
                    
                }
            }
        });
    }
    
    public void loadTable() {
        try {
            List<Shippers> list = cir.findAll();
            ctm.add(list);
            tabela.setModel(ctm);
            ctm.fireTableDataChanged();
        } catch (SCHMException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

    }
