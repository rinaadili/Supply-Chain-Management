    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        if (!ShippersTF.getText().trim().equals("") && !CompanyNameTF.getText().trim().equals("") && !PhoneTF.getText().trim().equals("") 
            && !EmailTF.getText().trim().equals("")&& isValidEmailAddress(EmailTF.getText())) {
            int row = tabela.getSelectedRow();
            if (row == -1) {
                Shippers s = new Shippers();
                s.setShipperID(Integer.parseInt(ShippersTF.getText()));
                s.setCompanyName(CompanyNameTF.getText());
                s.setPhone(PhoneTF.getText());
                s.setEmail(EmailTF.getText());
                
                try {
                    cir.create(s);
                } catch (SCHMException es) {
                    JOptionPane.showMessageDialog(this, es.getMessage());
                    return;
                }
            } else {
                Shippers s = ctm.getShippers(row);
                s.setShipperID(Integer.parseInt(ShippersTF.getText()));
                                s.setCompanyName(CompanyNameTF.getText());
                                s.setPhone(PhoneTF.getText());
                                s.setEmail(EmailTF.getText());

               
                try {
                    cir.edit(s);
                } catch (SCHMException es) {
                    JOptionPane.showMessageDialog(this, es.getMessage());
                    return;
                }
                
            }
            clearField();
            loadTable();

        } else {
            JOptionPane.showMessageDialog(this, "Ju lutem plotesoni te gjitha fushat obligative!");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

