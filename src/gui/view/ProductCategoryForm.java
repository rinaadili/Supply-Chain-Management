    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        if (!ProductCategoryTF.getText().trim().equals("") && !CategoryNameTF.getText().trim().equals("") && !DescriptionTF.getText().trim().equals("")) {
            int row = tabela.getSelectedRow();
            if (row == -1) {
                ProductCategory p = new ProductCategory();
                p.setCategoryID(Integer.parseInt(ProductCategoryTF.getText()));
                p.setCategoryName(CategoryNameTF.getText());
                p.setDescription(DescriptionTF.getText());
                
                try {
                    cir.create(p);
                } catch (SCHMException es) {
                    JOptionPane.showMessageDialog(this, es.getMessage());
                    return;
                }
            } else {
                ProductCategory p = ctm.getProductCategory(row);
                p.setCategoryID(Integer.parseInt(ProductCategoryTF.getText()));
                                p.setCategoryName(CategoryNameTF.getText());
                                p.setDescription(DescriptionTF.getText());

               
                try {
                    cir.edit(p);
                } catch (SCHMException es) {
                    JOptionPane.showMessageDialog(this, es.getMessage());
                    return;
                }
                
            }
            clearField();
            loadTable();

        } else {
            JOptionPane.showMessageDialog(this, "Ju lutem plotesoni te gjitha fushat obligative!");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

